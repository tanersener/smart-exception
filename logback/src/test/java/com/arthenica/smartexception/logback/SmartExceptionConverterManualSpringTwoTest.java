package com.arthenica.smartexception.logback;

import com.arthenica.smartexception.logback.spring.RestController;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.AutoConfigureMockMvc;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.request.MockMvcRequestBuilders;

import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@SpringBootTest
@AutoConfigureMockMvc
@ContextConfiguration(classes = RestController.class)
@RunWith(SpringJUnit4ClassRunner.class)
public class SmartExceptionConverterManualSpringTwoTest {

    @Autowired
    private MockMvc mvc;

    @Test
    public void getUserLoggerTest() {
        Logger logger = LoggerFactory.getLogger(this.getClass());

        try {
            mvc.perform(MockMvcRequestBuilders.get("/user")).andExpect(status().isOk());
        } catch (Exception e) {
            logger.error("Exception caught.", e);
        }
    }

}
